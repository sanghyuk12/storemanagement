package com.sm.storemanagement.dto;

public class UserDTO {
    String userNo;
    String userId;
    String useAt;

    public String getUserNo() {
        return userNo;
    }

    public void setUserNo() {
        this.userNo = userNo;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId() {
        this.userId = userId;
    }

    public String getUseAt() {
        return useAt;
    }

    public void setUseAt() {
        this.useAt = useAt;
    }

}
